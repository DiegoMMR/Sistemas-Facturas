    public void ingreso(String user, String pass)
    {
        //preparamos la sintaxis de sql para pedir los datos
        String sql = "SELECT * FROM Usuario WHERE Usuario='" + user + "';";
        String auxUser = "";
        String auxPass = "";
        String auxTipo = "";
        String nombre = "";
                
        
    try
    {
        Statement st = cn.createStatement();
        ResultSet rs = st.executeQuery(sql);
      
        //rescata de la base de datos
        while(rs.next())
        {
            auxUser = rs.getString("Usuario");
            auxPass = rs.getString("Contrase単a");
            auxTipo = rs.getString("Tipo");
            nombre = rs.getString("Nombre");
            
        }
        
       
        
    } catch (SQLException ex) {
        Logger.getLogger(InicioSecion.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(null, "Usuario o contrase単a in correctos");
        System.out.print("Error de conexion...." + ex);
    }
       
    
    if(user.equals(auxUser) && pass.equals(auxPass) )
    {
        //envia a la pagina proincipal
        Principal p = new Principal();
        p.setVisible(true);
        p.pack();
        //cambia el nombre del label 
        Principal.txtNombre.setText(nombre);
        Principal.txtTipo.setText(auxTipo);
        //cierra la ventana de inicioSecion
        dispose();
    }else{
      JOptionPane.showMessageDialog(null, "Usuario o contrase単a in correctos");
    }
    }
    
    
