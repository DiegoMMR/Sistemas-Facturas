    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        int result = JOptionPane.showConfirmDialog(null, "¿Estas seguro?", null, JOptionPane.YES_NO_OPTION);
        if (result == JOptionPane.YES_OPTION) {

            try {
                _Cliente.nombre = txtCliente.getText();
                _Cliente.nit = txtNit.getText();
                _Cliente.direccion = txtDireccion.getText();
                _Cliente.telefono = txtTelefono.getText();
                _Cliente.correo = txtCorreo.getText();

                con.editarCliente(_Cliente, nit);
                JOptionPane.showMessageDialog(null, "Cliente actualizado exitosamente");
            } catch (SQLException ex) {
                Logger.getLogger(EditarCliente.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Error al ingresar Error: " + ex.getMessage());
            }

        }
    }//GEN-LAST:event_jButton2ActionPerformed

